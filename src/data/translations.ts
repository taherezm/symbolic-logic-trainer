import type {
  EnglishToSymbolicExercise,
  LegendEntry,
  SymbolicToEnglishExercise,
} from '../types';

function englishToSymbolic(
  id: string,
  stage: EnglishToSymbolicExercise['stage'],
  sentence: string,
  legend: LegendEntry[],
  acceptedFormulas: string[],
  commonMistakes: EnglishToSymbolicExercise['commonMistakes'],
  skills: EnglishToSymbolicExercise['skills'],
  difficulty: EnglishToSymbolicExercise['difficulty'],
): EnglishToSymbolicExercise {
  return {
    id,
    type: 'english-to-symbolic',
    stage,
    title: 'English to Symbolic',
    prompt: 'Translate the sentence into symbolic form.',
    sentence,
    legend,
    acceptedFormulas,
    commonMistakes,
    skills,
    difficulty,
  };
}

function symbolicToEnglish(
  id: string,
  stage: SymbolicToEnglishExercise['stage'],
  formula: string,
  legend: LegendEntry[],
  options: SymbolicToEnglishExercise['options'],
  correctOptionId: string,
  skills: SymbolicToEnglishExercise['skills'],
  difficulty: SymbolicToEnglishExercise['difficulty'],
): SymbolicToEnglishExercise {
  return {
    id,
    type: 'symbolic-to-english',
    stage,
    title: 'Symbolic to English',
    prompt: 'Choose the best English translation.',
    formula,
    legend,
    options,
    correctOptionId,
    skills,
    difficulty,
  };
}

function pairLegend(
  first: [string, string],
  second: [string, string],
): LegendEntry[] {
  return [
    { symbol: first[0], meaning: first[1] },
    { symbol: second[0], meaning: second[1] },
  ];
}

function tripleLegend(
  first: [string, string],
  second: [string, string],
  third: [string, string],
): LegendEntry[] {
  return [
    { symbol: first[0], meaning: first[1] },
    { symbol: second[0], meaning: second[1] },
    { symbol: third[0], meaning: third[1] },
  ];
}

