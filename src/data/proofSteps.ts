import type { MultipleChoiceOption, ProofStepExercise } from '../types';

function proofStep(
  id: string,
  premises: string[],
  goal: string,
  options: MultipleChoiceOption[],
  correctOptionId: string,
  skills: ProofStepExercise['skills'],
  difficulty: ProofStepExercise['difficulty'],
  derived?: string[],
): ProofStepExercise {
  return {
    id,
    type: 'proof-step',
    stage: 'proof-basics',
    title: 'Best Next Step',
    prompt: 'Choose the single best next line. Pick the move that is legal now and moves closest to the goal.',
    premises,
    derived,
    goal,
    options,
    correctOptionId,
    skills,
    difficulty,
  };
}

