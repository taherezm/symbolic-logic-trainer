import type { ValidityExercise } from '../types';

function validityExercise(
  id: string,
  premises: string[],
  conclusion: string,
  argumentLabel: string,
  skills: ValidityExercise['skills'],
  difficulty: ValidityExercise['difficulty'],
  fallacyName?: string,
): ValidityExercise {
  return {
    id,
    type: 'validity',
    stage: 'validity-basics',
    title: 'Valid or Invalid',
    prompt: 'Do the premises guarantee the conclusion?',
    premises,
    conclusion,
    argumentLabel,
    skills,
    difficulty,
    fallacyName,
  };
}

